package Day22_NestedLoop;

public class Nested_Loop2 {
    public static void main(String[] args) {

        System.out.println();
        for(int j = 1; j <= 5 ; j++){

            for(int i = 1; i <= 5; i++){
                System.out.print(i+" ");
            }

            System.out.println();

        }

        System.out.println("====================================================");




    }
}