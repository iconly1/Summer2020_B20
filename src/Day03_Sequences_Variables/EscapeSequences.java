package Day03_Sequences_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello Everyone \nMy name is Cybertek ");
        System.out.println("\tIam in Virgina\n");
        System.out.println("\n\n\t\tI Love Programming");
        System.out.println("/"); // prints me one front slash
        System.out.println("\\");// prints me one backslash "has to be 2 in order to get one"
        System.out.println("My Name is 'Ian'");/* \' (escape sequence single quote)
        or you can use ' single quote */
        System.out.println("\"Ian\"");// \" Double quote has to be done with escape sequence




    }
}
