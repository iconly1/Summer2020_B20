package Day18_forLoop;

public class ForLoop_Practice {
    public static void main(String[] args) {


        for (int i = 1; i<=5; i++){
            System.out.print("Cybertek Batch 20");
        }
        System.out.println("============================");
        for (int i = 1; i<=10; i++){
            System.out.print(i+" ");
        }
        System.out.println();
        System.out.println("Hello");

        System.out.println("===============");

        for (int i = 100; i>=2; i--) {
                System.out.println(i);


        }




    }
}
